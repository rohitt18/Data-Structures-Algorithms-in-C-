#include <iostream>
using namespace std;

// Data types & Variables

int main()
{

    // int
    int a = 123;
    cout << a << endl;

    // char
    char b = 'v';
    // char b = 'vs';  (not possible bec single inverted commas mai hamesha single character hi hoga)
    cout << b << endl;

    // boolean
    // true => 1, false => 0
    bool bl = true;
    cout << bl << endl;

    // float
    float f = 1.2;
    cout << f << endl;

    // double
    double d = 2.34;
    cout << d << endl;

    // sizeof()
    int size = sizeof(a);
    cout << "size of a is : " << size << endl;
}